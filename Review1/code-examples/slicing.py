numbers = [0, 1, 2, 3, 4, 5]
"""
ASCII art analogy :)
 +---+---+---+---+---+---+
 | 0 | 1 | 2 | 3 | 4 | 5 | => Indices
 +---+---+---+---+---+---+
 0   1   2   3   4   5   6 => Borders
-6  -5  -4  -3  -2  -1
"""
numbers[0::2]  # => [0, 2, 4]
numbers[:]  # => [0, 1, 2, 3, 4, 5]
numbers[1:]  # => [1, 2, 3, 4, 5]
numbers[-2:]  # => [4, 5]
numbers[1:4]  # => [1, 2, 3]
numbers[1:1]  # => []
numbers[-99:99]  # => [0, 1, 2, 3, 4, 5]
numbers[::-1]  # => [5, 4, 3, 2, 1, 0]
numbers[::-2]  # => [5, 3, 1]